package azdblab.exception.procmonitor;

public class ProcMonitorException extends Exception{
	private static final long serialVersionUID =  System.identityHashCode("ProcMonitorException");
	/**
	 * Creates a proc monitor exception of this type.
	 */
	public ProcMonitorException() {
		super();
	}

	/**
	 * Creates an exception of this type with the specified message.
	 * @param message The message to be displayed with this exception.
	 */
	public ProcMonitorException(String message) {
		super(message);
	}

	/**
	 * Creates an exception of this type with the cause passed as a
	 * parameter and a message passed as a parameter.
	 * @param message The message that will be displayed for this exception.
	 * @param cause The cause of this exception.
	 */
	public ProcMonitorException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * Creates an exception of this type with the cause passed as a 
	 * parameter.
	 * @param cause The exception that is the cause of this exception.
	 */
	public ProcMonitorException(Throwable cause) {
		super(cause);
	}

}
