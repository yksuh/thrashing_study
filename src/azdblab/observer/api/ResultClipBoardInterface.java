package azdblab.observer.api;

/**
 * Used to perform clipboard actions on the GUI from the listener.
 * @author Kevan Holdaway
 *
 */
public interface ResultClipBoardInterface {
	/**
	 * Copies content from the GUI to the System Clipboard.
	 */
	public void copy();
	/**
	 * Cuts content from the GUI to the System Clipboard.
	 */
	public void cut();
	/**
	 * Pastes content from the System Clipboard to the GUI.
	 */
	public void paste();
}
