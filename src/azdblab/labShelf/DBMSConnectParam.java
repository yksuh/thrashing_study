package azdblab.labShelf;

public class DBMSConnectParam{
	
	public String		strNotebookName;
	
	public String		strJDBCUserName;
	public String		strJDBCPassword;
	public String		strJDBCConnectString;
	public String create_date_;
	public String creator_name_;
	public String comments_;
	
  public DBMSConnectParam(
      String notebookName, String jdbcUserName, String jdbcPassword,
      String jdbcConnectString,
      String create_date, String creator_name, String comments) {
		
		strNotebookName			= notebookName;
		strJDBCUserName			= jdbcUserName;
		strJDBCPassword			= jdbcPassword;
		strJDBCConnectString	= jdbcConnectString;
		create_date_ = create_date;
		creator_name_ = creator_name;
		comments_ = comments;
	}
	
	public String toString() {
		return strNotebookName;
	}
	
}
